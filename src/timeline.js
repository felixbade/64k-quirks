export function sampleTimeline(timeline, beat) {
  const cues = timeline.cues;
  if (!cues.length) throw new Error("timeline has no cues");
  let cue = cues[0];
  for (const c of cues) {
    if (c.beat <= beat) cue = c;
    else break;
  }
  return { shaderId: cue.shader, values: { ...cue.params } };
}
