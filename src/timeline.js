function seg(sceneName, from, beats, to = null) {
  if (to?.shader && to.shader !== from.shader) {
    throw new Error(`segment interpolation shader mismatch: ${from.shader} -> ${to.shader}`);
  }
  return {
    sceneName,
    shader: from.shader,
    beats,
    params: from.params,
    endParams: to ? (to.params ?? to) : null,
  };
}

