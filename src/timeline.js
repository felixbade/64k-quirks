function buildSegments(list) {
  let beat = 0;
  return list.map((s) => {
    const cue = { ...s, startBeat: beat, endBeat: beat + s.beats };
    beat += s.beats;
    return cue;
  });
}

function lerpParams(startParams, endParams, t) {
  const values = { ...startParams };
  for (const key in values) {
    const a = values[key];
    const b = endParams[key] ?? a;
    values[key] = a + (b - a) * t;
  }
  return values;
}

